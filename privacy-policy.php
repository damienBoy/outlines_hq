  <h1>Politique de confidentialité</h1>
  <p class="muted">
    Cette politique de confidentialité décrit la façon dont Outlines collecte, utilise et protège
    les données personnelles conformément au Règlement Général sur la Protection des Données (RGPD).
  </p>

  <h2>1. Responsable du traitement</h2>
  <p>
    Le responsable du traitement est l’association <strong>Outlines</strong>.<br>
    Contact : <a href="mailto:contact@outlines.fr">contact@outlines.fr</a>
  </p>

  <h2>2. Données collectées</h2>
  <p>
    Le site collecte uniquement les données nécessaires à l’inscription à la newsletter :
  </p>
  <ul>
    <li>Adresse email</li>
    <li>Consentement explicite (case à cocher)</li>
    <li>Date d’inscription</li>
  </ul>

  <h2>3. Finalités</h2>
  <p>
    Ces données sont utilisées exclusivement pour l’envoi de communications Outlines :
    annonces d’événements, sorties audio, actualités du collectif.
  </p>
  <p>
    Aucune donnée n’est vendue, louée ou transmise à un tiers.
  </p>

  <h2>4. Base légale</h2>
  <p>
    Le traitement repose sur le <strong>consentement explicite</strong> de l’utilisateur
    (article 6.1.a du RGPD), matérialisé par une case à cocher obligatoire.
  </p>

  <h2>5. Durée de conservation</h2>
  <p>
    Les données sont conservées jusqu’à la demande de désinscription de l’utilisateur,
    ou en cas d’inactivité prolongée de la base.
  </p>

  <h2>6. Sécurité</h2>
  <p>
    Les données sont stockées dans une base MySQL sécurisée, accessible uniquement par
    l’administrateur du site. Les échanges sont traités via des requêtes préparées PDO afin
    de limiter les risques d’injection SQL.
  </p>

  <h2>7. Droits des utilisateurs</h2>
  <p>
    Conformément au RGPD, chaque utilisateur dispose de :
  </p>
  <ul>
    <li>Droit d’accès</li>
    <li>Droit de rectification</li>
    <li>Droit d’effacement</li>
    <li>Droit d’opposition</li>
    <li>Droit de retrait du consentement</li>
  </ul>
  <p>
    Pour exercer ces droits : <a href="mailto:contact@outlines.fr">contact@outlines.fr</a>
  </p>

  <h2>8. Cookies</h2>
  <p>
    Le site Outlines n’utilise pas de cookies publicitaires ni de traceurs tiers.
    Si des cookies techniques sont ajoutés ultérieurement, cette politique sera mise à jour.
  </p>

  <h2>9. Mise à jour</h2>
  <p>
    Outlines se réserve le droit de modifier cette politique à tout moment.
    La version applicable est celle publiée sur le site à la date de consultation.
  </p>

